package com.example.chatapp.Model;

public class Users {
    String name;
    String username;
    String Id;

    public String getName() {
        return name;
    }

    public String getUsername() {
        return username;
    }

    public String getId() {
        return Id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public void setId(String id) {
        Id = id;
    }
}
